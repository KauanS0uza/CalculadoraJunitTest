package Classes;
public class Calculadora {
    // Setando as operações
    public float Soma(int a, int b){
        return a+b;
    }
    public float Substracao(int a, int b){
        return a-b;
    }
    public float Multiplicacao(int a, int b){
        return a*b;
    }
    public float Divisao(int a, int b){
        return a/b;
    }
}
